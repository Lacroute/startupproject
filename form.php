<p class="text-form"><strong>Formulaire d'inscription Startup
à l'AACC CAP DIGITAL STARTUP PROJECT 2014</p></strong>

<p>Seules les candidatures complètes et envoyées avant le jeudi 8 janvier seront prises en compte et évaluées. 
Les données fournies serviront à présélectionner 20 startups qui pitcheront le jeudi 22 janvier et vendredi 23 janvier 2015 leur proposition de valeur devant les parties prenantes du programme. 
Les startups présélectionnées bénéficieront d’une visibilité sur la place de marché digital développée en coopération par l’AACC et Cap Digital sur ce même site. Les données du présent formulaire seront utilisées pour constituer les fiches profils de ces startups.<br>
</p>


